public class PathPoints {

    float x;
    float y;

    public PathPoints(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getY() {
        return this.y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
